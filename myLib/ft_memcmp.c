#include"libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	unsigned char	*r1;
	unsigned char	*r2;

	r1 = (unsigned char *)s1;
	r2 = (unsigned char *)s2;
	i = 0;
	if (!n)
		return (0);
	while (r1[i] == r2[i] && i < n - 1)
		i++;
	return (r1[i] - r2[i]);
}
